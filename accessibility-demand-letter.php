    <section id="intro">
        <p>You wake up to an email with a legal letter attached. It's from a lawyer claiming your website violates the Americans with Disabilities Act (ADA). Your site is inaccessible. You have violations. They want compensation or they'll sue.</p>
        <p>Your first instinct might be panic. Your second might be to ignore it. Both are mistakes.</p>
        <p>Accessibility demand letters are real. They're increasingly common. And how you respond in the next 30 days will determine whether you settle for $5,000, pay $50,000 in legal fees, or end up in prolonged litigation. This guide walks you through what to do, what it means, and how to minimize damage.</p>
