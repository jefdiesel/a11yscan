    <section id="types-of-legal-support">
        <h2>Types of Legal Support Available</h2>
        
        <h3>1. Disability Rights Organizations</h3>
        <p>Non-profit disability advocacy organizations often provide legal support, either directly or through partnerships:</p>
        <ul>
            <li>Many offer free or low-cost legal consultations</li>
            <li>Some have staff attorneys who handle accessibility cases</li>
            <li>They understand disability discrimination in depth</li>
            <li>They can advise on whether your case is viable</li>
            <li>They often work on systemic accessibility issues</li>
        </ul>
        <p>Start by contacting disability rights organizations in your state or national organizations focused on accessibility.</p>

        <h3>2. Private Attorneys Specializing in Disability Rights</h3>
        <p>Some private law firms specialize in ADA and disability rights litigation:</p>
        <ul>
            <li>They have deep expertise in disability discrimination law</li>
            <li>They can handle individual cases and class actions</li>
            <li>They understand the technical aspects of web accessibility</li>
            <li>They may work on contingency (payment depends on winning)</li>
            <li>Consultation fees vary; some offer free initial consultations</li>
        </ul>

        <h3>3. Civil Rights Attorneys</h3>
        <p>General civil rights attorneys may handle accessibility cases, though with less specialization:</p>
        <ul>
            <li>They understand ADA law but may not specialize in web accessibility</li>
            <li>Useful if you combine accessibility claims with other discrimination</li>
            <li>May need education on technical accessibility issues</li>
            <li>Check their experience with similar cases</li>
        </ul>

        <h3>4. Legal Aid Organizations</h3>
        <p>If you have limited income, legal aid may be available:</p>
        <ul>
            <li>Free legal services for low-income individuals</li>
            <li>May not have accessibility specialists on staff</li>
            <li>Can refer you to appropriate resources</li>
            <li>Eligibility varies by location and income</li>
        </ul>
    </section>

    <section id="finding-attorneys">
        <h2>How to Find Qualified Attorneys</h2>
        
        <h3>Step 1: Start with Disability Rights Organizations</h3>
        <p><strong>Most Effective First Step</strong></p>
        <p>These organizations are often the best entry point because they:</p>
        <ul>
            <li>Know who handles accessibility cases in your area</li>
            <li>Have relationships with qualified attorneys</li>
            <li>Can vet cases before referring to counsel</li>
            <li>Often provide free or low-cost initial consultation</li>
        </ul>
        <p>Search for: "[Your State] disability rights organization" or "[Your State] accessibility advocacy group"</p>

        <h3>Step 2: Check Bar Association Referral Services</h3>
        <p>State bar associations maintain referral services:</p>
        <ul>
            <li>Search their website for attorneys listing "accessibility" or "ADA"</li>
            <li>Bar associations often have practice area directories</li>
            <li>Some offer free consultation as part of referral service</li>
            <li>Verify the attorney is in good standing</li>
        </ul>

        <h3>Step 3: Search Online Legal Directories</h3>
        <p>Use specialized legal search platforms:</p>
        <ul>
            <li>Search terms: "ADA attorney," "disability rights lawyer," "web accessibility attorney"</li>
            <li>Check their listed expertise areas and past cases</li>
            <li>Look for client reviews and case results (when available)</li>
            <li>Call for consultation (many offer free initial calls)</li>
        </ul>

        <h3>Step 4: Ask in Disability Communities</h3>
        <p>Get recommendations from other people with disabilities:</p>
        <ul>
            <li>Online disability communities and forums</li>
            <li>Local disability support groups</li>
            <li>Social media groups focused on accessibility</li>
            <li>Word-of-mouth recommendations from people with similar experiences</li>
        </ul>

        <h3>Step 5: Contact Law Schools</h3>
        <p>Some law schools have clinics focused on disability rights:</p>
        <ul>
            <li>Clinical professors who specialize in accessibility</li>
            <li>Law students trained in disability rights</li>
            <li>Often provide free or low-cost services</li>
            <li>May work on innovative or test cases</li>
        </ul>
