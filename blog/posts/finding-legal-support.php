    <section id="evaluating-attorneys">
        <h2>What to Look for in an Attorney</h2>
        
        <h3>Experience with Web Accessibility Cases</h3>
        <p>Ask potential attorneys:</p>
        <ul>
            <li>"Have you handled web accessibility discrimination cases?"</li>
            <li>"How many ADA Title III cases have you brought?"</li>
            <li>"Are you familiar with WCAG 2.1 standards?"</li>
            <li>"Have you worked with technical experts or accessibility consultants?"</li>
        </ul>
        <p>Ideal candidates have handled multiple accessibility cases or have strong ADA litigation background.</p>

        <h3>Understanding of Disability</h3>
        <p>Does the attorney:</p>
        <ul>
            <li>Respect disability as a civil rights issue (not charity)?</li>
            <li>Understand different types of disabilities and their impacts?</li>
            <li>Listen to your experience without dismissing barriers?</li>
            <li>Communicate without ableist language or assumptions?</li>
        </ul>
        <p>You want someone who gets why this matters beyond the legal technicality.</p>

        <h3>Technical Understanding</h3>
        <p>They should be able to:</p>
        <ul>
            <li>Explain WCAG standards in understandable terms</li>
            <li>Understand how assistive technology works</li>
            <li>Work with accessibility experts and engineers</li>
            <li>Review technical evidence and expert reports</li>
        </ul>
        <p>They don't need to be a technologist, but they need comfort with technical concepts.</p>

        <h3>Communication Style</h3>
        <p>Does the attorney:</p>
        <ul>
            <li>Explain things clearly without jargon?</li>
            <li>Answer your questions and return calls promptly?</li>
            <li>Explain the costs and fee structure upfront?</li>
            <li>Discuss realistic expectations for your case?</li>
        </ul>

        <h3>Contingency Representation</h3>
        <p>Many accessibility attorneys work on contingency:</p>
        <ul>
            <li>You pay nothing unless you win</li>
            <li>Attorney's fee comes from your settlement or judgment</li>
            <li>You may still pay for court costs and expert witnesses</li>
            <li>Get fee arrangements in writing</li>
        </ul>
