    <section id="prepare-for-consultation">
        <h2>Preparing for Your Initial Consultation</h2>
        
        <h3>Gather Your Documentation</h3>
        <p>Before meeting with an attorney, prepare:</p>
        <ul>
            <li>Timeline of your attempts to access the website</li>
            <li>Screenshots and videos showing the barriers</li>
            <li>Copies of any communication with the website owner</li>
            <li>Technical details (which assistive technology, what specifically failed)</li>
            <li>Information about the website operator (size, type of business)</li>
            <li>Notes on how this affected you (lost business, missed services, etc.)</li>
        </ul>

        <h3>Write Out Your Story</h3>
        <p>Put together a brief narrative:</p>
        <ul>
            <li>When you tried to use the website</li>
            <li>What you were trying to accomplish</li>
            <li>How the barrier prevented you from accessing services</li>
            <li>What you tried to do about it</li>
            <li>What outcome you're seeking</li>
        </ul>

        <h3>Questions to Ask the Attorney</h3>
        <ul>
            <li>"Do you think I have a viable case?"</li>
            <li>"What would I need to prove?"</li>
            <li>"What are the likely outcomes?"</li>
            <li>"What's your fee structure?"</li>
            <li>"What resources would I need to invest?"</li>
            <li>"How long would this likely take?"</li>
            <li>"Would we pursue settlement or litigation?"</li>
        </ul>
    </section>

    <section id="case-viability">
        <h2>Will an Attorney Take Your Case?</h2>
        
        <h3>Factors That Make Cases More Viable</h3>
        <ul>
            <li>Clear, documented barriers affecting core functionality</li>
            <li>Website owner is a business or organization (public accommodation)</li>
            <li>Multiple attempts to contact them with no response</li>
            <li>Significant harm or damages (lost services, time, etc.)</li>
            <li>Evidence the barrier affects many people, not just you</li>
            <li>Website operator is large enough to afford remediation</li>
        </ul>

        <h3>Factors That Make Cases Less Viable</h3>
        <ul>
            <li>Website is government-only (different legal pathway)</li>
            <li>Website is purely informational with no impact</li>
            <li>Barrier is extremely minor or easily circumvented</li>
            <li>Website operator is genuinely attempting compliance</li>
            <li>Limited damages or clear remedy available</li>
        </ul>

        <h3>What Attorneys Cannot Do</h3>
        <p>Be realistic about legal remedies:</p>
        <ul>
            <li>They cannot force agencies to enforce accessibility</li>
            <li>They cannot make laws or change federal standards</li>
            <li>They cannot sue social media companies easily (still unclear in law)</li>
            <li>They cannot guarantee a specific outcome</li>
            <li>They cannot sue on your behalf without evidence you're damaged</li>
        </ul>
    </section>

    <section id="alternatives-to-litigation">
        <h2>Alternatives to Full Litigation</h2>
        
        <h3>Demand Letters</h3>
        <p>Your attorney sends a formal legal letter demanding remediation:</p>
        <ul>
            <li>Often prompts quick response from website owners</li>
            <li>Much less expensive than litigation</li>
            <li>Gives owner clear legal notice and timeframe</li>
            <li>Creates record if litigation becomes necessary</li>
        </ul>

        <h3>Settlement Negotiations</h3>
        <p>Many cases settle before trial:</p>
        <ul>
            <li>Website owner agrees to fix barriers and pay damages</li>
            <li>Faster than litigation, more certain outcome</li>
            <li>Attorney can negotiate on your behalf</li>
            <li>Can include compensation for your time and harm</li>
        </ul>

        <h3>Administrative Complaints</h3>
        <p>For government websites, you can file complaints:</p>
        <ul>
            <li>Department of Justice (federal sites)</li>
            <li>State human rights commissions</li>
            <li>State attorney general's office</li>
            <li>No attorney needed to file, but helpful to have one</li>
        </ul>

        <h3>Class Action Potential</h3>
        <p>If many people are affected, your case might become a class action:</p>
        <ul>
            <li>Your attorney represents a larger group with similar claims</li>
            <li>More leverage to push for systemic change</li>
            <li>Broader impact than individual settlement</li>
            <li>Individual class members may receive less compensation</li>
        </ul>
