    <section id="unruh-act">
        <h2>The Unruh Civil Rights Act</h2>
        <p>The Unruh Civil Rights Act, passed in 1959, predates the internet but has been successfully applied to digital accessibility by California courts. It prohibits businesses from discriminating based on disability in providing services.</p>
        
        <h3>Key Characteristics</h3>
        <ul>
            <li>Applies to any "business establishment" serving the public</li>
            <li>Interpretation expanded to include websites and digital services</li>
            <li>Private enforcement: Individuals can sue directly</li>
            <li>Damages: Minimum $4,000 per violation per day</li>
            <li>Attorney fees awarded to prevailing plaintiffs</li>
        </ul>
        
        <h3>Damages Under Unruh</h3>
        <ul>
            <li>Statutory damages: $4,000 minimum per violation</li>
            <li>Actual damages if greater than statutory amount</li>
            <li>Calculated per day of violation</li>
            <li>Multiple violations = cumulative damages</li>
            <li>Attorney fees in addition to damages</li>
        </ul>
        
        <p>Official text: <a href="https://leginfo.legislature.ca.gov/faces/codes_displayText.xhtml?lawCode=CC&division=3.&title=1.&part=4.&chapter=2.">California Civil Code Section 51 (Unruh Act)</a></p>
    </section>

    <section id="ab-434">
        <h2>AB 434: State Digital Accessibility Mandate</h2>
        <p>Assembly Bill 434, effective January 1, 2018, requires California state agencies and departments to maintain accessible websites and web applications.</p>
        
        <h3>Requirements</h3>
        <ul>
            <li>All state websites must meet WCAG 2.0 Level AA</li>
            <li>Web applications must be accessible</li>
            <li>PDFs and digital documents must be accessible</li>
            <li>Applies to all state agencies and departments</li>
            <li>Annual reporting to Legislature required</li>
        </ul>
        
        <h3>Impact on Businesses</h3>
        <p>While AB 434 directly applies only to state agencies, it established WCAG 2.0 Level AA as the official California standard. Courts have referenced AB 434 when interpreting Unruh Act accessibility requirements.</p>
        
        <p>Official details: <a href="https://leginfo.legislature.ca.gov/faces/billTextClient.xhtml?bill_id=201720180AB434">AB 434 Legislative Text</a></p>
    </section>

