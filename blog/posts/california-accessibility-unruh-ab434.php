    <section id="accessibility-standard">
        <h2>Accessibility Standards: WCAG 2.0 AA</h2>
        <p>California law recognizes WCAG 2.0 Level AA as the accessibility standard for websites and digital content.</p>
        
        <h3>WCAG 2.0 Level AA Requirements</h3>
        <ul>
            <li>4.5:1 color contrast for normal text</li>
            <li>3:1 contrast for large text (18pt+)</li>
            <li>Keyboard accessible navigation</li>
            <li>Alt text for all images</li>
            <li>Captions for video content</li>
            <li>Proper heading hierarchy</li>
            <li>Form labels and error messages</li>
            <li>Screen reader compatibility</li>
            <li>Page resizable to 200% without loss of function</li>
        </ul>
        
        <p>Reference: <a href="https://www.w3.org/WAI/WCAG20/quickref/">WCAG 2.0 Quick Reference</a></p>
