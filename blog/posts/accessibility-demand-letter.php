            <li style="margin-bottom: 0.75rem;">WCAG criterion violated</li>
            <li style="margin-bottom: 0.75rem;">Severity assessment</li>
            <li style="margin-bottom: 0.75rem;">Remediation recommendations</li>
            <li style="margin-bottom: 0.75rem;">Estimated effort to fix</li>
        </ul>
