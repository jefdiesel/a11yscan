    <section id="what-is-demand-letter">
        <h2>What Is an Accessibility Demand Letter?</h2>
        <p>An accessibility demand letter is a formal legal notice claiming your website violates ADA Title III or similar accessibility laws.</p>
        <p>The letter typically includes:</p>
        <ul style="list-style: disc; padding-left: 1.5rem; margin-bottom: 1rem;">
            <li style="margin-bottom: 0.75rem;"><strong>Specific accessibility violations:</strong> Details of features that don't work (broken keyboard navigation, missing alt text, poor color contrast, etc.)</li>
            <li style="margin-bottom: 0.75rem;"><strong>A demand for compensation:</strong> Usually $5,000-$25,000 for initial letters</li>
            <li style="margin-bottom: 0.75rem;"><strong>A deadline:</strong> Often 30 days to respond</li>
            <li style="margin-bottom: 0.75rem;"><strong>A litigation threat:</strong> "If you don't settle, we'll file a lawsuit"</li>
            <li style="margin-bottom: 0.75rem;"><strong>Attorney contact information:</strong> Where to send your response</li>
        </ul>
        <p>These letters are often sent by accessibility law firms specializing in ADA claims. Understanding that this is a formal legal notice requiring prompt, professional response is critical.</p>
