            <li style="margin-bottom: 0.75rem;"><strong>Do NOT ignore it.</strong> Ignoring the letter typically triggers a lawsuit.</li>
            <li style="margin-bottom: 0.75rem;"><strong>Do NOT respond immediately without counsel.</strong> You have time. Use it strategically.</li>
            <li style="margin-bottom: 0.75rem;"><strong>Alert your legal team and decision-makers.</strong> Get your insurance agent, lawyers, and business leadership involved immediately.</li>
        </ul>
        <p>The goal of this step is to preserve evidence and buy yourself thinking time for proper decision-making.</p>
