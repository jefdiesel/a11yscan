        <h2>Who Must Make Their Website Accessible?</h2>
        
        <h3>Clearly Covered (Business Websites)</h3>
        <p>These entities almost certainly must have accessible websites:</p>
        <ul>
            <li>Retail businesses (online stores, local shops with websites)</li>
            <li>Banks and financial institutions</li>
            <li>Hospitals and healthcare providers</li>
            <li>Transportation services</li>
            <li>Hotels and vacation rentals</li>
            <li>Insurance companies</li>
            <li>Real estate agents and brokers</li>
            <li>Educational institutions accepting federal funding</li>
        </ul>

        <h3>Sometimes Covered (It Depends)</h3>
        <p>The accessibility requirement depends on whether the entity is considered a "public accommodation" or receives federal funding:</p>
        <ul>
            <li>Non-profit organizations (generally covered if they serve the public)</li>
            <li>Professional associations</li>
            <li>Online platforms and marketplaces</li>
            <li>Social media companies (unclear, but increasingly being challenged)</li>
        </ul>

        <h3>Less Clear (Evolving Law)</h3>
        <p>Courts are still deciding whether these need accessible websites:</p>
        <ul>
            <li>Purely informational websites (not transactional)</li>
            <li>Small businesses (though size is not a legal exemption)</li>
            <li>Online-only businesses with no physical location</li>
            <li>Third-party platforms (responsibility unclear)</li>
        </ul>
