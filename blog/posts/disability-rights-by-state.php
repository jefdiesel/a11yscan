        <h2>What This Means for You in Practice</h2>
        
        <h3>You Have the Right To:</h3>
        <ul>
            <li>Access a website using keyboard navigation if you cannot use a mouse</li>
            <li>Use screen reader software to understand website content</li>
            <li>Adjust text size and color contrast without breaking the site</li>
            <li>Access all functionality available to non-disabled users</li>
            <li>Get alternative formats (e.g., transcripts for videos, descriptions for images)</li>
            <li>Navigate and use forms without barriers</li>
            <li>Receive equivalent access to services offered online</li>
        </ul>

        <h3>You Have the Right To Complaint and Remedy</h3>
        <p>If a website is inaccessible, you generally have the right to:</p>
        <ul>
            <li>Contact the website owner and request accessible alternatives</li>
            <li>File a complaint with federal agencies (for government sites)</li>
            <li>File a complaint with state human rights agencies</li>
            <li>Pursue legal action to enforce your rights and seek remedies</li>
            <li>Work with advocacy organizations and attorneys</li>
        </ul>
    </section>

