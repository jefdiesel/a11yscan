    <section id="ada-title-iii">
        <h2>ADA Title III: The Foundation of Web Accessibility Rights</h2>
        <p>The Americans with Disabilities Act (ADA) is the primary federal law protecting your right to equal access. Title III specifically addresses public accommodations—businesses and organizations that serve the public. For over 30 years, courts have increasingly recognized that websites operated by public accommodations must be accessible.</p>
        <p>Title III applies to businesses like retailers, restaurants, banks, hotels, transportation services, and healthcare providers. If these organizations have websites, those sites must be accessible to people with disabilities. The standard generally accepted for accessibility compliance is WCAG 2.1, published by the World Wide Web Consortium (W3C).</p>
        <p>However, there's no federal agency that enforces web accessibility under Title III. There's no website inspector checking for keyboard navigation or alt text. The responsibility falls on you—people with disabilities—to identify barriers and assert your rights.</p>
    </section>

    <section id="state-local-laws">
        <h2>State and Local Accessibility Laws</h2>
        
        <h3>States with Specific Web Accessibility Requirements</h3>
        <p>Several states have enacted laws that explicitly require web accessibility for government agencies and sometimes private businesses:</p>
        <ul>
            <li><strong>California:</strong> Generally recognized as having strong accessibility protections through court precedent and consumer protection laws</li>
            <li><strong>New York:</strong> Has specific accessibility requirements for websites, particularly through its Human Rights Law</li>
            <li><strong>Massachusetts:</strong> Known for web accessibility standards, particularly 504 regulations affecting state contractors</li>
            <li><strong>Colorado, Illinois, Vermont, and Washington:</strong> Have enacted or proposed specific web accessibility standards</li>
        </ul>
        <p>If you live in a state without explicit web accessibility laws, you may still have protections under state disability rights laws, consumer protection statutes, or state interpretations of the ADA.</p>

        <h3>Government Websites vs. Private Businesses</h3>
        <p>Government websites are subject to even stricter requirements. Federal, state, and local government sites must comply with Section 508 of the Rehabilitation Act, which typically requires WCAG 2.1 Level AA compliance or higher. Many states have adopted similar standards for state government websites.</p>
        <p>Private businesses have more flexibility in how they demonstrate accessibility, but must still provide equal access to people with disabilities under Title III of the ADA.</p>
    </section>

