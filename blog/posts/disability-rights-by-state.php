        <h2>Important Limitations and Gaps</h2>
        
        <h3>The Enforcement Problem</h3>
        <p>While your rights are real, enforcement is complicated. The Department of Justice can investigate Title III complaints, but does not have robust resources for web accessibility enforcement. This means the burden falls primarily on individuals with disabilities to identify barriers and pursue remedies.</p>

        <h3>Geographic Variation</h3>
        <p>Your specific rights depend significantly on where the website operator is located, where you live, and whether you're dealing with a state or federal matter. What's considered a violation in California may be treated differently in another state.</p>

        <h3>Evolving Standards</h3>
        <p>Courts continue to develop web accessibility law. What constitutes adequate accessibility compliance is still being litigated. Technical standards are also evolving—WCAG 2.1 Level AA is widely recognized, but courts haven't uniformly mandated this standard.</p>

        <h3>No Private Right of Action for Section 508</h3>
        <p>Section 508 (the law governing federal websites) does not give you a direct right to sue. You must file a complaint through administrative channels.</p>
